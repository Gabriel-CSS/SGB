    private void BotaoFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoFinalizarActionPerformed
        try{
            Conexao c = new Conexao();
            if(c.atualiza_locacao(Integer.parseInt(idLocBaixa.getText())) == true){
                JOptionPane.showMessageDialog(rootPane, "Baixa realizada com sucesso!");
                dispose();
            }else{
                JOptionPane.showMessageDialog(rootPane, "Erro ao dar baixa, tente corrigir os campos!");
            }
            idLocBaixa.setText("");
            diasUltrapassados.setText("");
            Multa.setText("");
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Erro ao dar baixa, tente corrigir os campos!");
        } 
    }//GEN-LAST:event_BotaoFinalizarActionPerformed

    private void CalculaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculaButtonActionPerformed
        try {
            if(Integer.parseInt(diasUltrapassados.getText()) >= 0){    
                String multa = String.format("%.2f",(Integer.parseInt(diasUltrapassados.getText())) * (0.5));       
                Multa.setText("R$" + multa);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Insira um valor positivo!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Insira os dias corretamente!");
        }
        
    }//GEN-LAST:event_CalculaButtonActionPerformed

