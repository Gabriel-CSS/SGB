    public void buscaLocaNome(int busca, int tipo){
        Conexao c = new Conexao();
        if(busca > 0 ){
        try{
        ResultSet rs = c.BuscarClienteLocacao(busca, tipo);
        DefaultTableModel modelo = new DefaultTableModel(  
        new Object[] {"ID","Devolvido","Data de Devolução", "Prazo(dias)", "ID Livro", "ID Cliente"},0){
            boolean[] canEdit = new boolean [] { false, false, false, false, false, false};    
            @Override  
            public boolean isCellEditable(int rowIndex, int columnIndex){  
                return canEdit [columnIndex];  
            }  
        };
        TabelaBaixa.setModel(modelo);
        while(rs.next()){
        modelo.addRow(new String[]{rs.getString("id_locacao"),
                rs.getString("devolvido"), rs.getString("data_devolucao"), rs.getString("prazo_devolucao"), 
                rs.getString("id_livro_FK"), rs.getString("id_cliente_FK")} );
        }
        
        }catch(SQLException e){JOptionPane.showMessageDialog(rootPane, "Erro na busca");}
        }else{
            JOptionPane.showMessageDialog(null,"Insira um ID válido");
        }
    }
    
    
